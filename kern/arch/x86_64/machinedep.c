#define X86_PIC_1_COMMAND (0x20)
#define X86_PIC_1_DATA (0x21)

#define X86_PIC_2_COMMAND (0xA0)
#define X86_PIC_2_DATA (0xA1)


static IDTDescriptor IDT[256];
static IDTLocation IDTPTR;

