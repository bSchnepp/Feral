#include "defines.h"

#include <feral/stdtypes.h>
#include <feral/feralstatus.h>

#if defined(__x86_64__) || defined(__i386__)
/* hack to deal with arch-specific stuff */
void x86_io_stall()
{
	volatile int i = 0;
	volatile int b = 1;
	i = i + b;
	return;
}

UINT64 x86_read_cr3()
{
	return 0;
}

VOID x86_write_cr3(UINT64 cr3)
{
	volatile UINT64 Copy = cr3;
	Copy += 1;
	Copy -= 1;
}
#endif

/* stubs for some assembly stuff the kernel expects. Maybe fixme? 
 */
void *kern_start;
void *kern_end;

typedef TEST_STATUS (*TestCase)(VOID);

TEST_STATUS CompilerCheck(VOID)
{
	ASSERT_EQ_LLD(0, 0, "CompilerCheck", "Zero Equals Zero");
	ASSERT_NEQ_LLD(0, 1, "CompilerCheck", "Zero Equals One");
	ASSERT_EQ_STR("0", "0", "CompilerCheck", "Zero Equals Zero");
	ASSERT_NEQ_STR("0", "1", "CompilerCheck", "Zero Equals One");
	return TEST_STATUS_OK;
}

TestCase Cases[] = 
{
	CompilerCheck,
};

/* function pointer array is uniform size, unlike something like utf-8,
 * so every entry is the same size. Go ahead and use that to get sizeof array.
 */
#define UNIFORM_SIZE(x) (sizeof(x) / sizeof(x[0]))

int main(int argc, char **argv)
{
	for (UINT64 Index = 0; Index < UNIFORM_SIZE(Cases); ++Index)
	{
		TestCase Func = Cases[Index];
		TEST_STATUS Status = Func();
		if (Status != TEST_STATUS_OK)
		{
			return Status;
		}
	}
	return 0;
}
